require "rails_helper"

RSpec.describe FinancesController, type: :routing do
  describe "routing" do
    it "routes to #index" do
      expect(get: "/finances").to route_to("finances#index")
    end

    it "routes to #new" do
      expect(get: "/finances/new").to route_to("finances#new")
    end

    it "routes to #show" do
      expect(get: "/finances/1").to route_to("finances#show", id: "1")
    end

    it "routes to #edit" do
      expect(get: "/finances/1/edit").to route_to("finances#edit", id: "1")
    end


    it "routes to #create" do
      expect(post: "/finances").to route_to("finances#create")
    end

    it "routes to #update via PUT" do
      expect(put: "/finances/1").to route_to("finances#update", id: "1")
    end

    it "routes to #update via PATCH" do
      expect(patch: "/finances/1").to route_to("finances#update", id: "1")
    end

    it "routes to #destroy" do
      expect(delete: "/finances/1").to route_to("finances#destroy", id: "1")
    end
  end
end
